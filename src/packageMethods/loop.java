package packageMethods;

public class loop {

	public static void main(String[] args) 
	
	{
	
//i want to print table 5
//intialisation 	condition	updation
		//5				<=50	+5
		
		
		
		int i=5;
		
		do
		{
			System.out.println(i);//statement to be executed //5,10,45
			i=i+5;//updation 10,15.........45
			
		}while(i<=50);
		
		System.out.println("=======================");
		
		//i want to print table 5
		//intialisation 	condition	updation
		//50				>=5				-5		
		
		int j=50;
		do
		{
			System.out.println(j);
			j=j-5;
		}while(j>=5);
		
		
		
		
		
		
		
		
		
		
	}

}
